//Single quote example

<?php
$name = "Anthony";

echo 'Hello $name\n';
?>

//Double quote example

<?php
$name = "Anthony";

echo "Hello $name\n";
?>

/*
The difference between single quotes ('') and double quotes ("") in PHP
is that items inside single quotes are treated literally, while items
inside double quotes are interpolated. This means that a variable name
inside double quotes is replaced with its value.
*/
